import { Principal } from '@dfinity/principal';

/**
 * Derives the ICP Ledger Account Identifier from a Principal.
 * Uses the standard derivation with default subaccount (all zeros).
 * 
 * Formula: account_identifier = CRC32(h) || h
 * where h = sha224("\x0Aaccount-id" || principal || subaccount)
 */
export function principalToAccountId(principal: Principal): string {
  const subAccount = new Uint8Array(32); // Default subaccount (all zeros)
  
  // Build the hash input: "\x0Aaccount-id" || principal || subaccount
  const prefix = new TextEncoder().encode('\x0Aaccount-id');
  const principalBytes = principal.toUint8Array();
  
  const hashInput = new Uint8Array(prefix.length + principalBytes.length + subAccount.length);
  hashInput.set(prefix, 0);
  hashInput.set(principalBytes, prefix.length);
  hashInput.set(subAccount, prefix.length + principalBytes.length);
  
  // Compute SHA-224 hash
  const hash = sha224(hashInput);
  
  // Compute CRC32 checksum of the hash
  const crc = crc32(hash);
  
  // Concatenate CRC32 (4 bytes) + hash (28 bytes) = 32 bytes total
  const accountId = new Uint8Array(32);
  accountId.set(crc, 0);
  accountId.set(hash, 4);
  
  // Convert to hex string
  return Array.from(accountId)
    .map(b => b.toString(16).padStart(2, '0'))
    .join('');
}

/**
 * SHA-224 hash implementation
 */
function sha224(data: Uint8Array): Uint8Array {
  // SHA-224 is SHA-256 with different initial values and truncated output
  const K = [
    0x428a2f98, 0x71374491, 0xb5c0fbcf, 0xe9b5dba5, 0x3956c25b, 0x59f111f1, 0x923f82a4, 0xab1c5ed5,
    0xd807aa98, 0x12835b01, 0x243185be, 0x550c7dc3, 0x72be5d74, 0x80deb1fe, 0x9bdc06a7, 0xc19bf174,
    0xe49b69c1, 0xefbe4786, 0x0fc19dc6, 0x240ca1cc, 0x2de92c6f, 0x4a7484aa, 0x5cb0a9dc, 0x76f988da,
    0x983e5152, 0xa831c66d, 0xb00327c8, 0xbf597fc7, 0xc6e00bf3, 0xd5a79147, 0x06ca6351, 0x14292967,
    0x27b70a85, 0x2e1b2138, 0x4d2c6dfc, 0x53380d13, 0x650a7354, 0x766a0abb, 0x81c2c92e, 0x92722c85,
    0xa2bfe8a1, 0xa81a664b, 0xc24b8b70, 0xc76c51a3, 0xd192e819, 0xd6990624, 0xf40e3585, 0x106aa070,
    0x19a4c116, 0x1e376c08, 0x2748774c, 0x34b0bcb5, 0x391c0cb3, 0x4ed8aa4a, 0x5b9cca4f, 0x682e6ff3,
    0x748f82ee, 0x78a5636f, 0x84c87814, 0x8cc70208, 0x90befffa, 0xa4506ceb, 0xbef9a3f7, 0xc67178f2
  ];

  // SHA-224 initial hash values (different from SHA-256)
  let h0 = 0xc1059ed8;
  let h1 = 0x367cd507;
  let h2 = 0x3070dd17;
  let h3 = 0xf70e5939;
  let h4 = 0xffc00b31;
  let h5 = 0x68581511;
  let h6 = 0x64f98fa7;
  let h7 = 0xbefa4fa4;

  // Pre-processing
  const msgLen = data.length;
  const bitLen = msgLen * 8;
  
  // Padding
  const paddedLen = Math.ceil((msgLen + 9) / 64) * 64;
  const padded = new Uint8Array(paddedLen);
  padded.set(data);
  padded[msgLen] = 0x80;
  
  // Append length as 64-bit big-endian
  const view = new DataView(padded.buffer);
  view.setUint32(paddedLen - 4, bitLen >>> 0, false);

  // Process each 512-bit chunk
  for (let chunkStart = 0; chunkStart < paddedLen; chunkStart += 64) {
    const w = new Uint32Array(64);
    
    // Copy chunk into first 16 words
    for (let i = 0; i < 16; i++) {
      w[i] = view.getUint32(chunkStart + i * 4, false);
    }
    
    // Extend into remaining 48 words
    for (let i = 16; i < 64; i++) {
      const s0 = rightRotate(w[i - 15], 7) ^ rightRotate(w[i - 15], 18) ^ (w[i - 15] >>> 3);
      const s1 = rightRotate(w[i - 2], 17) ^ rightRotate(w[i - 2], 19) ^ (w[i - 2] >>> 10);
      w[i] = (w[i - 16] + s0 + w[i - 7] + s1) >>> 0;
    }
    
    // Initialize working variables
    let a = h0, b = h1, c = h2, d = h3, e = h4, f = h5, g = h6, h = h7;
    
    // Main loop
    for (let i = 0; i < 64; i++) {
      const S1 = rightRotate(e, 6) ^ rightRotate(e, 11) ^ rightRotate(e, 25);
      const ch = (e & f) ^ (~e & g);
      const temp1 = (h + S1 + ch + K[i] + w[i]) >>> 0;
      const S0 = rightRotate(a, 2) ^ rightRotate(a, 13) ^ rightRotate(a, 22);
      const maj = (a & b) ^ (a & c) ^ (b & c);
      const temp2 = (S0 + maj) >>> 0;
      
      h = g;
      g = f;
      f = e;
      e = (d + temp1) >>> 0;
      d = c;
      c = b;
      b = a;
      a = (temp1 + temp2) >>> 0;
    }
    
    // Add chunk hash to result
    h0 = (h0 + a) >>> 0;
    h1 = (h1 + b) >>> 0;
    h2 = (h2 + c) >>> 0;
    h3 = (h3 + d) >>> 0;
    h4 = (h4 + e) >>> 0;
    h5 = (h5 + f) >>> 0;
    h6 = (h6 + g) >>> 0;
    h7 = (h7 + h) >>> 0;
  }
  
  // Produce final hash (SHA-224 is first 224 bits = 28 bytes)
  const result = new Uint8Array(28);
  const resultView = new DataView(result.buffer);
  resultView.setUint32(0, h0, false);
  resultView.setUint32(4, h1, false);
  resultView.setUint32(8, h2, false);
  resultView.setUint32(12, h3, false);
  resultView.setUint32(16, h4, false);
  resultView.setUint32(20, h5, false);
  resultView.setUint32(24, h6, false);
  
  return result;
}

/**
 * CRC32 checksum implementation
 */
function crc32(data: Uint8Array): Uint8Array {
  const table = new Uint32Array(256);
  
  // Build CRC32 table
  for (let i = 0; i < 256; i++) {
    let c = i;
    for (let j = 0; j < 8; j++) {
      c = (c & 1) ? (0xEDB88320 ^ (c >>> 1)) : (c >>> 1);
    }
    table[i] = c;
  }
  
  // Calculate CRC32
  let crc = 0xFFFFFFFF;
  for (let i = 0; i < data.length; i++) {
    crc = table[(crc ^ data[i]) & 0xFF] ^ (crc >>> 8);
  }
  crc = crc ^ 0xFFFFFFFF;
  
  // Convert to big-endian bytes
  const result = new Uint8Array(4);
  result[0] = (crc >>> 24) & 0xFF;
  result[1] = (crc >>> 16) & 0xFF;
  result[2] = (crc >>> 8) & 0xFF;
  result[3] = crc & 0xFF;
  
  return result;
}

/**
 * Right rotate helper for SHA-224
 */
function rightRotate(n: number, bits: number): number {
  return ((n >>> bits) | (n << (32 - bits))) >>> 0;
}
